

using System;

class Program

{
    static void Main(string[] args)
    {
        // Declare a variable to hold the user's name
        string name;
        // Prompt the user for their name
        Console.Write("Please enter your name: ");

        // Read the user's input and store it in the variable
        name = Console.ReadLine();
        // Display a greeting message using the user's name
        Console.WriteLine($"Hello, {name}! Welcome to the program.");
    }
}









